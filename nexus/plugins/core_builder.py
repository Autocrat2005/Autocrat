    def build_plugin(self, description: str = "", query: str = "", **kwargs) -> Dict[str, Any]:
        """Generate a new plugin from a natural-language description."""
        desc = description or query
        if not desc:
            return {
                "success": False,
                "error": "Please provide a description of the plugin you want to build.\n"
                         "Example: build plugin that checks internet speed and latency",
            }

        log.info(f"🏗️ Building plugin: {desc}")

        # ── Step 1: Ask LLM to generate the spec ──
        try:
            prompt = CODE_GEN_PROMPT.format(description=desc)
            raw = self._call_llm(prompt, temperature=0.15, num_predict=3000)
        except Exception as e:
            return {"success": False, "error": f"LLM call failed: {e}"}

        spec = self._parse_json_response(raw)
        if not spec:
            return {
                "success": False,
                "error": "LLM returned invalid JSON. Try rephrasing your description.",
                "raw_output": raw[:500],
            }

        plugin_name = spec.get("plugin_name", "").strip()
        if not plugin_name:
            return {"success": False, "error": "LLM didn't provide a plugin_name", "spec": spec}

        # Sanitize name
        plugin_name = re.sub(r"[^a-z0-9_]", "_", plugin_name.lower()).strip("_")
        spec["plugin_name"] = plugin_name

        # ── Step 2: Assemble source code ──
        source = self._assemble_plugin(spec)

        # ── Step 3: Validate ──
        ok, err = self._full_validate(source)
        if not ok:
            # Try to get LLM to fix it (one attempt)
            log.warning(f"Generated code failed validation: {err}")
            fix_prompt = (
                f"The following Python plugin code has validation errors:\n"
                f"ERRORS: {err}\n\n"
                f"SOURCE:\n```python\n{source}\n```\n\n"
                f"Return the COMPLETE fixed Python source code. No markdown fences. "
                f"No explanation. Just the corrected Python code."
            )
            try:
                fixed = self._call_llm(fix_prompt, temperature=0.05, num_predict=3000)
                if fixed.startswith("```"):
                    fixed = re.sub(r"^```(?:python)?\s*", "", fixed)
                    fixed = re.sub(r"\s*```\s*$", "", fixed)
                ok2, err2 = self._full_validate(fixed)
                if ok2:
                    source = fixed
                    log.info("LLM self-corrected the validation errors")
                else:
                    return {
                        "success": False,
                        "error": f"Generated code failed validation even after self-correction.\n"
                                 f"Original: {err}\nAfter fix: {err2}",
                    }
            except Exception as fix_e:
                return {"success": False, "error": f"Validation failed ({err}), and fix attempt errored: {fix_e}"}

        # ── Step 4: Write file ──
        filepath = self._write_plugin_file(plugin_name, source)

        # ── Step 4.5: Network permission check ──
        clear, perm_msg, blocked_domains = self._check_network_permissions(plugin_name, source)
        if not clear:
            # Pause the load — push a permission prompt
            self._pending_permissions[plugin_name] = {
                "domains": blocked_domains,
                "filepath": str(filepath),
                "source": source,
                "spec": spec,
            }
            return {
                "success": True,
                "requires_permission": True,
                "result": (
                    f"🏗️ Plugin '{plugin_name}' generated and validated!\n\n"
                    f"⚠️  [SECURITY] This plugin wants network access to:\n"
                    f"   {', '.join(blocked_domains)}\n\n"
                    f"Choose an option:\n"
                    f"  [1] Allow Once  → grant permission {plugin_name} once\n"
                    f"  [2] Allow Always (add to config) → grant permission {plugin_name} always\n"
                    f"  [3] Block & Delete → grant permission {plugin_name} block"
                ),
                "plugin_name": plugin_name,
                "blocked_domains": blocked_domains,
                "commands": [
                    f"grant permission {plugin_name} once",
                    f"grant permission {plugin_name} always",
                    f"grant permission {plugin_name} block",
                ],
                "file": str(filepath),
            }

        # ── Step 5: Hot-load ──
        ok, err = self._hot_load(plugin_name, filepath)
        if not ok:
            return {
                "success": False,
                "error": f"Plugin generated but failed to load: {err}",
                "file": str(filepath),
            }

        # ── Success! ──
        engine = self._get_engine()
        plugin = engine.plugins.get(plugin_name) if engine else None
        cmd_count = len(plugin.get_commands()) if plugin else 0

        return {
            "success": True,
            "result": (
                f"🏗️ Plugin '{plugin_name}' built and loaded!\n"
                f"   📦 {cmd_count} commands registered\n"
                f"   📁 {filepath}\n"
                f"   🔥 Hot-loaded into NEXUS — ready to use!"
            ),
            "plugin_name": plugin_name,
            "commands": [c.get("name") for c in spec.get("commands", [])],
            "file": str(filepath),
        }

    def list_generated(self, **kwargs) -> Dict[str, Any]:
        """List all dynamically generated plugins."""
        if not self._generated:
            return {
                "success": True,
                "result": "No generated plugins yet. Use 'build plugin <description>' to create one!",
            }

        engine = self._get_engine()
        lines = ["🏗️ Generated Plugins:"]
        for name, path in self._generated.items():
            loaded = "✅ loaded" if (engine and name in engine.plugins) else "⚪ not loaded"
            lines.append(f"  • {name} — {loaded} ({path.name})")

        return {"success": True, "result": "\n".join(lines), "plugins": list(self._generated.keys())}

    def unload_generated(self, name: str = "", query: str = "", **kwargs) -> Dict[str, Any]:
        """Unload a generated plugin."""
        target = name or query
        if not target:
            return {"success": False, "error": "Specify the plugin name to unload"}

        target = target.strip().lower().replace(" ", "_")
        if target not in self._generated:
            return {"success": False, "error": f"'{target}' is not a generated plugin",
                    "available": list(self._generated.keys())}

        engine = self._get_engine()
        if engine and target in engine.plugins:
            engine.unload_plugin(target)
            engine.gemini.update_commands(engine.get_all_commands())

        return {"success": True, "result": f"Plugin '{target}' unloaded."}

    def reload_generated(self, name: str = "", query: str = "", **kwargs) -> Dict[str, Any]:
        """Reload (hot-swap) a generated plugin from disk."""
        target = name or query
        if not target:
            return {"success": False, "error": "Specify the plugin name to reload"}

        target = target.strip().lower().replace(" ", "_")
        filepath = self._generated.get(target)
        if not filepath or not filepath.exists():
            return {"success": False, "error": f"No generated file found for '{target}'",
                    "available": list(self._generated.keys())}

        ok, err = self._hot_load(target, filepath)
        if not ok:
            return {"success": False, "error": f"Reload failed: {err}"}

        return {"success": True, "result": f"🔥 Plugin '{target}' reloaded!"}

    def heal_plugin(self, name: str = "", query: str = "",
                    error: str = "", **kwargs) -> Dict[str, Any]:
        """Auto-debug and fix a broken generated plugin."""
        target = name or query
        if not target:
            return {"success": False, "error": "Specify the plugin name to heal"}

        target = target.strip().lower().replace(" ", "_")
        filepath = self._generated.get(target)
        if not filepath or not filepath.exists():
            return {"success": False, "error": f"No generated file found for '{target}'"}

        error_trace = error or "Plugin reported an error during execution (no specific traceback)"

        ok, msg, patched = self._auto_heal(
            target,
            error_trace=error_trace,
            action="manual_heal",
            args={},
        )

        if ok:
            return {"success": True, "result": f"🩺 {msg}"}
        else:
            return {"success": False, "error": f"Heal failed: {msg}"}

    # ─── Auto-Load Previously Generated Plugins ──────────────────────────────

    def _handle_grant_permission(self, name: str = "", query: str = "",
                                   mode: str = "once", **kwargs) -> Dict[str, Any]:
        """
        Handle the grant permission command from user.
        Usage: grant permission <plugin_name> <once|always|block>
        """
        target = name or query
        if not target:
            return {"success": False, "error": "Specify the plugin name and mode (once / always / block)"}

        # Parse "plugin_name mode" from a single string
        parts = target.strip().split()
        if len(parts) >= 2:
            target = parts[0]
            mode = parts[-1]
        target = target.strip().lower().replace(" ", "_")
        mode = mode.strip().lower()

        if mode not in ("once", "always", "block"):
            return {
                "success": False,
                "error": f"Invalid mode '{mode}'. Use: once, always, or block",
            }

        pending = self._pending_permissions.get(target)
        if not pending:
            return {
                "success": False,
                "error": f"No pending permission request for '{target}'",
                "available": list(self._pending_permissions.keys()),
            }

        domains = pending["domains"]

        # Don't pop yet — grant_network_permission needs the filepath from pending
        return self.grant_network_permission(target, domains, mode)

    def auto_load_all_generated(self):
        """Load all previously generated plugins (called by engine after startup)."""
        loaded = 0
        failed = 0
        deferred = 0
        for name, filepath in self._generated.items():
            if filepath.exists():
                try:
                    source = filepath.read_text(encoding="utf-8")
                    ok, err = self._full_validate(source)
                    if not ok:
                        log.warning(f"Skipping invalid generated plugin '{name}': {err}")
                        failed += 1
                        continue

                    # Check network permissions before auto-loading
                    clear, perm_msg, blocked_domains = self._check_network_permissions(name, source)
                    if not clear:
                        self._pending_permissions[name] = {
                            "domains": blocked_domains,
                            "filepath": str(filepath),
                            "source": source,
                        }
                        log.warning(
                            f"⚠️  [SECURITY] Plugin '{name}' deferred — needs permission "
                            f"for: {', '.join(blocked_domains)}. "
                            f"Run: grant permission {name} once/always/block"
                        )
                        deferred += 1
                        continue

                    success, load_err = self._hot_load(name, filepath)
                    if success:
                        loaded += 1
                    else:
                        log.warning(f"Failed to auto-load generated plugin '{name}': {load_err}")
                        failed += 1
                except Exception as e:
                    log.warning(f"Error auto-loading '{name}': {e}")
                    failed += 1

        if loaded or failed or deferred:
            log.info(f"🏗️ Auto-loaded {loaded} generated plugins ({failed} failed, {deferred} awaiting permission)")
