"""
NEXUS OS — Core Builder (Meta-Plugin)
Autonomous plugin generation, AST validation, hot-swap loading, and auto-debugging.

This plugin lets NEXUS write its own Python plugins at runtime:
  1. LLM generates plugin code from a natural-language description
  2. AST sandbox validates syntax + scans for unsafe patterns
  3. Hot-swap loader writes, imports, and registers the plugin with the engine
  4. Auto-debugger catches errors, feeds traceback to LLM, patches, retries

Commands:
  • build_plugin  — generate + validate + hot-load a new plugin
  • list_generated — list all dynamically generated plugins
  • unload_plugin  — unload a generated plugin by name
  • reload_plugin  — reload (hot-swap) an existing generated plugin
  • heal_plugin    — auto-debug a broken generated plugin
"""

